    private void deletebtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deletebtnActionPerformed
        // TODO add your handling code here:
        DefaultTableModel tblModel=(DefaultTableModel)menutable.getModel();
        if(menutable.getSelectedRowCount()==1){
            int id=Integer.parseInt(tblModel.getValueAt(menutable.getSelectedRow(),2).toString());
            String mess=new MenuControl().deleteItem(id);
            if(mess!="null"){
                tblModel.removeRow(menutable.getSelectedRow());
                JOptionPane.showMessageDialog(this,mess);
            }
            else{
                JOptionPane.showMessageDialog(this,"Some Error Occured, Please Try Again !");
            }   
        }
        else{
            if(menutable.getSelectedRowCount()==0){
                JOptionPane.showMessageDialog(this,"Table is Empty..");
            }
            else{
                JOptionPane.showMessageDialog(this,"Please! Select Single Row to Delete");
            }
        }
    }//GEN-LAST:event_deletebtnActionPerformed

