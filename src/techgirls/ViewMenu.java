    private void updatebtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updatebtnActionPerformed
        // TODO add your handling code here:
        DefaultTableModel tblModel=(DefaultTableModel)menutable.getModel();
        if(menutable.getSelectedRowCount()==1){
            //foodname.setText(tblModel.getValueAt(menutable.getSelectedRow(),0).toString());
            //price.setText(tblModel.getValueAt(menutable.getSelectedRow(),1).toString());
            if(!foodname.getText().equals("") && !price.getText().equals(""))
            {
                String food=foodname.getText();
                String pricep=price.getText();
                int id=Integer.parseInt(tblModel.getValueAt(menutable.getSelectedRow(),2).toString());
                tblModel.setValueAt(food,menutable.getSelectedRow(),0);
                tblModel.setValueAt(pricep,menutable.getSelectedRow(),1);
                String mess=new MenuControl().updateMenu(id,food,pricep);
                System.out.println("mess "+mess);
            
                if(mess!="null"){
                    JOptionPane.showMessageDialog(this,mess);
                    
                }
                else JOptionPane.showMessageDialog(this,"Some Error Occured, Please Try Again!");
                foodname.setText("");
                price.setText("");
                menutable.getSelectionModel().clearSelection();
            }
        }
        else{
            if(menutable.getSelectedRowCount()==0){
                JOptionPane.showMessageDialog(this,"Select a row to be updated..");
            }
            else{
                JOptionPane.showMessageDialog(this,"Please! Select Single Row for Update");
            }
        }
    }//GEN-LAST:event_updatebtnActionPerformed

